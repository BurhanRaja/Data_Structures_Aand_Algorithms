package com.Burhan;

public class OR_Operator {
    public static void main(String[] args) {
        // int num1 = 5;
        // int num2 = 7;
        
        // the answer here is 111 i.e. 7.
        // In AND operator the answer would have been 101 i.e. 5.

        int num1 = 10;
        int num2 = 29;
        // The answer here is 11111 i.e. 31.
        // In AND operator the answer would have been 1000 i.e. 8.

        System.out.println((num1|num2));
    }
}
