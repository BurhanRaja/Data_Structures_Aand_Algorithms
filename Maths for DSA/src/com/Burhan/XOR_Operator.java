package com.Burhan;

public class XOR_Operator {
    public static void main(String[] args) {
        // int num1 = 6; // 110, 7 is 111.
        // Complement of 6 is 7 and 7 is 6.

        int num1 = 2;
        
        System.out.println((num1^1)); // Complement
        System.out.println((num1^0)); // number itself
        System.out.println((num1^num1)); // Zero

    }
}
